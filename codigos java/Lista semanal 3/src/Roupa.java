
public class Roupa {
	
	private Cor cor;
	private Tamanho tamanho;
	private Estado estado;
	
	public void setcor(Cor c) {
		cor = c;
		
	}
	public Cor getcor() {
		return cor;
	}
	public void settamanho(Tamanho t) {
		tamanho = t;
		
	}
	public Tamanho gettamanho() {
		return tamanho;
	}
	public void setestado(Estado e) {
		estado = e;
		
	}
	public Estado getestado() {
		return estado;
	}



}
